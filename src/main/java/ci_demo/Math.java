package ci_demo;

public class Math {
	
	/***
	 * Sum the numbers
	 * @param a
	 * @param b
	 * @return the sum as integer
	 */
	public static int sum(int a, int b) {
		return a+b;
	}
	
	/***
	 * Divide the numbers
	 * @param a
	 * @param b
	 * @return the result as integer
	 */
	public static int divide(int a, int b) {
		return (int) a/b;
	}
}
